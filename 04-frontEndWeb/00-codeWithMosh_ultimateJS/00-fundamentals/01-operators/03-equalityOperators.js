/**
 *  STRICT EQUALITY OPERATOR
 * 
 *  Ensures both sides of the operators have the type && value.
 *  More precise
 *  More accurate
 */
console.log(1 === 1); // TRUE
console.log('1' === 1); // FALSE

/**
 *  LOOSE EQUALITY OPERATOR
 * 
 *  Ensures both sides of the operator have the same value.
 */
console.log(1 == 1); // TRUE
console.log('1' == 1); // TRUE
console.log(true == 1) // TRUE